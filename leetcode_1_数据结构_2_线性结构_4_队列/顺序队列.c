/*****************************************************************************
 * 描  述: 元素elem队列
 * 参数说明：
 * 返回值：
*****************************************************************************/
int enQueue(int *a,int rear,int data){
    a[rear]=data;
    rear++;
    return rear;
}




/*****************************************************************************
 * 描  述: 数据元素出队列
 * 参数说明：
 * 返回值：
*****************************************************************************/
void deQueue(int *a,int front,int rear){
    //如果 front==rear，表示队列为空
    while (front!=rear) {
        printf("出队元素：%d\n",a[front]);
        front++;
    }
}

