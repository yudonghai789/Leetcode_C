QNode * initQueue(){
    QNode * queue=(QNode*)malloc(sizeof(QNode));
    queue->next=NULL;
    return queue;
}


/*****************************************************************************
 * 描  述: 元素elem队列
 * 参数说明：
 * 返回值：
*****************************************************************************/
QNode* enQueue(QNode * rear,int data){
    QNode * enElem=(QNode*)malloc(sizeof(QNode));
    enElem->data=data;
    enElem->next=NULL;
    //使用尾插法向链队列中添加数据元素
    rear->next=enElem;
    rear=enElem;
    return rear;
}




/*****************************************************************************
 * 描  述: 数据元素出队列
 * 参数说明：
 * 返回值：
*****************************************************************************/
QNode* DeQueue(QNode * top,QNode * rear){
    QNode * p = NULL;
    if (top->next==NULL) {
        printf("\n队列为空");
        return rear;
    }
    p=top->next;
    printf("%d ",p->data);
    top->next=p->next;
    if (rear==p) {
        rear=top;
    }
    free(p);
    return rear;
}

