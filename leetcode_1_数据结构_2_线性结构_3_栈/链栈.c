/*****************************************************************************
 * 描  述: 元素elem进栈
 * 参数说明：
 * 返回值：
*****************************************************************************/
lineStack* push(lineStack * stack,int a){
    lineStack * line=(lineStack*)malloc(sizeof(lineStack));
    line->data=a;
    line->next=stack;
    stack=line;
    return stack;
}



/*****************************************************************************
 * 描  述: 数据元素出栈
 * 参数说明：
 * 返回值：
*****************************************************************************/
lineStack * pop(lineStack * stack){
    if (stack) {
        lineStack * p=stack;
        stack=stack->next;
        printf("弹栈元素：%d ",p->data);
        if (stack) {
            printf("栈顶元素：%d\n",stack->data);
        }else{
            printf("栈已空\n");
        }
        free(p);
    }else{
        printf("栈内没有元素");
        return stack;
    }
    return stack;
}

