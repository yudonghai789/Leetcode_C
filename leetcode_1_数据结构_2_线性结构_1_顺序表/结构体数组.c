//创建数组的函数
Array array_create(int init_size){
	Array a;
	a.size=init_size;
	a.array=(int*)malloc(sizeof(int)*init_size);
	return a;
} 


/*****************************************************************************
 * 描  述: 数组长度
 * 参数说明：
 * 返回值：
*****************************************************************************/
//封装数组长度
int array_length(const Array *a){
	return a->size;
} 





/*****************************************************************************
 * 描  述: 插入结点
 * 参数说明：
 * 返回值：
*****************************************************************************/
//增
void array_inflate(Array *a,int more_size){
	int *p=(int*)malloc(sizeof(int)*(a->size+more_size));
	for (int i=0;i<a->size;i++){
		p[i]=a->array[i];
	}
	free(a->array);
	a->array=p;
	a->size+=more_size;
} 





/*****************************************************************************
 * 描  述: 删除结点
 * 参数说明：
 * 返回值：
*****************************************************************************/
//删 
void array_delete(Array* a,int index){
	if (index<a->size){
		if (index==(a->size-1)){
			//直接在尾部长度减1 
		}
		else{
			for (int i=index;i<a->size;i++){
				a->array[i]=a->array[i+1];
			}
		} 
	a->size--;	
	}
	else{
		printf("您输入删除的位置超过数组长度，产生越界"); 
	} 
}





/*****************************************************************************
 * 描  述: 查找
 * 参数说明：
 * 返回值：
*****************************************************************************/
//查 
int array_at(Array *a,int index)
{
	if(index<a->size){
		return a->array[index];
	}
	else{
		printf("您输入删除的位置超过数组长度，产生越界"); 
		return 0;
	} 
}


/*****************************************************************************
 * 描  述: 修改
 * 参数说明：
 * 返回值：
*****************************************************************************/
//改
void array_set(Array *a,int value,int index){
	if(index<a->size){
		a->array[index]=value;	
	}
	else{
		printf("您输入删除的位置超过数组长度，产生越界"); 
	} 
} 




